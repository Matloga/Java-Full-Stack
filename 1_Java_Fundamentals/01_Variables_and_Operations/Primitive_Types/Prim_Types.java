
public class Prim_Types {
    public static void main(String[] args) {
        // 1. byte
        byte age = 30;
        System.out.println("Byte value (age): " + age);

        // 2. short
        short temperature = -150;
        System.out.println("Short value (temperature): " + temperature);

        // 3. int
        int salary = 60000;
        System.out.println("Int value (salary): " + salary);

        // 4. long
        long population = 7000000000L;
        System.out.println("Long value (population): " + population);

        // 5. float
        float price = 19.99f;
        System.out.println("Float value (price): " + price);

        // 6. double
        double pi = 3.141592653589793;
        System.out.println("Double value (pi): " + pi);

        // 7. char
        char grade = 'B';
        System.out.println("Char value (grade): " + grade);

        // 8. boolean
        boolean isJavaFun = true;
        System.out.println("Boolean value (isJavaFun): " + isJavaFun);
    }
}